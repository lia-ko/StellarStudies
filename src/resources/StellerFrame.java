    //evaluate answer with True False options and mark it with symbol
    private int checkTrueFalse(JRadioButton rbTrue, JRadioButton rbFalse, JLabel jlCheck, boolean answer) {
        //variable for points for correct answer
        int intPoint = 0;

        //mark answer as correct
        if ((rbTrue.isSelected() && answer) || (rbFalse.isSelected() && !answer)) {
            jlCheck.setText(Character.toString(chOK));
            jlCheck.setForeground(Color.GREEN);
            intPoint = 1;
        } else {
            //mark answer as incorrect
            jlCheck.setText(Character.toString(chNotOK));
            jlCheck.setForeground(Color.RED);
        }

        return intPoint;
    }

    //evaluate answer supplied as a text and mark it with symbol
    private int checkString(JTextField jtEntry, JLabel jlCheck, JLabel jlAns) {
        //variable to keep answer and for points for correct answer
        String strAns;
        int intPoint = 0;

        //read user's answer
        strAns = jtEntry.getText();
        
        //mark answer as correct
        if (strAns.equalsIgnoreCase(jlAns.getText())) {
            jlCheck.setText(Character.toString(chOK));
            jlCheck.setForeground(Color.GREEN);
            jlAns.setVisible(false);
            intPoint = 1;
        } else {
            //mark answer as incorrect
            jlCheck.setText(Character.toString(chNotOK));
            jlCheck.setForeground(Color.RED);
            jlAns.setVisible(true);
        }

        return intPoint;
    }

    //check if correct option was selected and return a point for that
    private int checkTestOptions(JRadioButton rb) {
        //variable for points for correct answer
        int intPoint = 0;

        //correct answer
        if (rb.isSelected()) {
            intPoint = 1;
        }

        return intPoint;
    }
    
    //evaluate answer supplied as a text and return point for it
    private int checkTestString(JTextField jtEntry, String strAns) {
        //variable for points for correct answer
        int intPoint = 0;

        //correct answer
        if (strAns.equalsIgnoreCase(jtEntry.getText())) {
            intPoint = 1;
        } 

        return intPoint;
    }
    
